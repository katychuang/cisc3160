// single line comments start with //

// main is the starting point for every executable Rust program (this is like c!)
fn main() {

    // declare a variable with the string
    let my_string = "Hello World!";

    // print output with the variable
    println!("\n{}\n\n", my_string);
    let a = "abc";
    let b = &a;
    println!("\n{}\n", b);
}
